package com.example.biteaslice;

public class StaticRvModelClass {

    private int image;
    private String text;

    public StaticRvModelClass(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }


    public String getText() {
        return text;
    }

}
